var xyz=1214234;

//convert number to string

console.log("converting number to string",xyz.toString());
 
//convert number to exponential
console.log("converting number to string",xyz.toExponential(2));

//convert number to fixed method
console.log("converting number to string",xyz.toFixed(3));

//convert number to precision
console.log("converting number to string",xyz.toPrecision());

//convert to valueof
console.log("converting number to string",xyz.valueOf());